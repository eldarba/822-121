package e.string.concat;

public class Demo1 {

	public static void main(String[] args) {
		String s1 = "aaa";
		String s2 = "bbb";
		String s3 = s1 + " " + s2;
		System.out.println(s1);
		System.out.println(s2);
		System.out.println(s3);

		int a = 6;
		int b = 11;

		System.out.println(a + " + " + b + " = " + (a + b));
	}
}
