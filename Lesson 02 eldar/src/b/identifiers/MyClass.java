// identifiers (names) - Java naming conventions

// package identifier is lower case separated with dots .
package b.identifiers;

// class identifier - camel case, must start with a capital letter
public class MyClass {

	// variable identifier - camel case, must start with a lower case letter
	private int x;
	private int num;
	private int val;
	private int maxVal;
	private int numberOfCustomers;

	// method identifier - camel case, must start with a lower case letter
	public void myMethd() {
		// note:
//		System - is a class identifier
//		out - is a variable identifier
//		println - is a method identifier
		System.out.println("Hello World!");
	}

}
