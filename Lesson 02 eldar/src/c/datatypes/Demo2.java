package c.datatypes;

public class Demo2 {

	public static void main(String[] args) {

		System.out.println(10);
		System.out.println(10L);
		System.out.println(10F);
		System.out.println(10D);

		System.out.println(3.33333333333333333333333333333333333333333);
		System.out.println(3.33333333333333333333333333333333333333333F);

		// machine time is a long value
		System.out.println(System.currentTimeMillis());

		long x = 99999999999L;

		int a = 5;
		int b = 6;
		System.out.println((a + b) / 2D);

	}
}
