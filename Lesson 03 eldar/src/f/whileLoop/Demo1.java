package f.whileLoop;

public class Demo1 {

	public static void main(String[] args) {

		int c = 1;

		// while loop
		while (c <= 3) {
			System.out.println(c);
			c++;
		}
		System.out.println("===============");

		// write a loop that prints the numbers 5 to 15
		c = 5;
		while (c <= 15) {
			System.out.println(c++);
		}
	}

}
